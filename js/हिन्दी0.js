// --- 3. D3 RENDER LOGIC ---
const width = window.innerWidth;
const height = window.innerHeight;

const svg = d3.select("#chart-container")
    .append("svg")
    .attr("width", "100%")
    .attr("height", "100vh");

const g = svg.append("g");

// ZOOM
const zoom = d3.zoom()
    .scaleExtent([0.1, 4])
    .on("zoom", e => g.attr("transform", e.transform));

svg.call(zoom);

// TREE
const tree = d3.tree().nodeSize([45, 280]);
const root = d3.hierarchy(treeData);

// COLLAPSE ALL
function collapseFull(d) {
    if (d.children) {
        d._children = d.children;
        d._children.forEach(collapseFull);
        d.children = null;
    }
}
collapseFull(root);

root.x0 = 0;
root.y0 = 0;

// CENTER
function getCenter() {
    return {
        x: window.innerHeight / 2,
        y: window.innerWidth / 2
    };
}

function centerPoint(x, y, scale = 0.7) {
    const c = getCenter();

    const t = d3.zoomIdentity
        .translate(
            c.y - y * scale,
            c.x - x * scale
        )
        .scale(scale);

    svg.transition()
        .duration(600)
        .call(zoom.transform, t);
}

function focusNewNodes(parent) {
    if (!parent.children || parent.children.length === 0) {
        centerPoint(parent.x, parent.y);
        return;
    }

    let sumX = 0;
    let sumY = 0;

    parent.children.forEach(c => {
        sumX += c.x;
        sumY += c.y;
    });

    centerPoint(
        sumX / parent.children.length,
        sumY / parent.children.length
    );
}


// UPDATE
let i = 0;
const tooltip = d3.select("#node-tooltip");

function update(source) {

    const nodes = tree(root).descendants();
    const links = tree(root).links();

    nodes.forEach(d => d.y = d.depth * 280);

    const node = g.selectAll("g.node")
        .data(nodes, d => d.id || (d.id = ++i));

    const nodeEnter = node.enter()
        .append("g")
        .attr("class", "node")
        .attr("transform", `translate(${source.y0},${source.x0})`)
        .on("click", (event, d) => {

    const hadChildren = !!d.children;

    if (d.children) {
        d._children = d.children;
        d.children = null;
    } else {
        d.children = d._children;
        d._children = null;
    }

    update(d);

    // ⚠️ layout complete होने के बाद focus
    requestAnimationFrame(() => {
        if (!hadChildren) {
            focusNewNodes(d);   // expand
        } else {
            centerPoint(d.x, d.y); // collapse
        }
    });
})

        .on("mousemove", (event, d) => {
            const info = nodeInfo[d.data.name] || "इस नोड की जानकारी उपलब्ध नहीं है।";
            tooltip
                .style("left", event.clientX + 12 + "px")
                .style("top", event.clientY + 12 + "px")
                .style("opacity", 1)
                .html(`<strong>${d.data.name}</strong><br>${info}`);
        })
        .on("mouseleave", () => tooltip.style("opacity", 0));

    nodeEnter.append("circle")
        .attr("r", 1e-6)
        .style("fill", d => d._children ? "var(--highlight)" : "#000");

    nodeEnter.append("text")
        .attr("dy", ".35em")
        .attr("x", d => d.children || d._children ? -18 : 18)
        .attr("text-anchor", d => d.children || d._children ? "end" : "start")
        .text(d => d.data.name);

    nodeEnter.merge(node)
        .transition()
        .duration(500)
        .attr("transform", d => `translate(${d.y},${d.x})`)
        .select("circle")
        .attr("r", 8)
        .style("fill", d => d._children ? "var(--highlight)" : "#000");

    node.exit().remove();

    const link = g.selectAll("path.link")
        .data(links, d => d.target.id);

    link.enter()
        .insert("path", "g")
        .attr("class", "link")
        .merge(link)
        .transition()
        .duration(500)
        .attr("d", d => `
            M ${d.source.y} ${d.source.x}
            C ${(d.source.y + d.target.y) / 2} ${d.source.x},
              ${(d.source.y + d.target.y) / 2} ${d.target.x},
              ${d.target.y} ${d.target.x}
        `);

    link.exit().remove();

    nodes.forEach(d => {
        d.x0 = d.x;
        d.y0 = d.y;
    });
}

// INIT
update(root);
centerPoint(root.x, root.y); 



